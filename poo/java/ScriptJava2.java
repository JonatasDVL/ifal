package ScriptsJava;

public class ScriptJava2 {

	public static void main(String[] args) {

		// declarando a idade atual
		int idade;
		idade = 16;
		
		//declarando a próxima idade
		int proxima_idade;
		proxima_idade=idade+1;
		
		//imprimindo a idade atual
		System.out.println(" Eu tenho "+ idade +" anos.");
		//imprimindo a próxima idade
		System.out.println(" Completarei "+ proxima_idade +" anos.");
	}
}
