package ScriptsJava;

public class ScriptJava3 {

	public static void main(String[] args) {
		
		//irei fazer algumas operações 
		
		//declarei o valor da soma e imprimir
		int soma;
		soma=2+3;
		
		System.out.println(" A soma entre 2 mais 3 é "+ soma +".");

		//declarei o valor da subtração e imprimir
		int subtração;
		subtração=9-3;
		
		System.out.println(" A subtração entre 9 menos 3 é "+ subtração +".");
		
		//declarei o valor da multiplicação e imprimir
		int multiplicação;
		multiplicação=3*7;
		
		System.out.println(" A multiplicação entre 3 vezes 7 é "+ multiplicação +".");
		
		//declarei o valor da divisão e imprimir
		int divisão;
		divisão=10/5;
		
		System.out.println(" A divisão entre 10 divido 5 é "+ divisão +".");

		//declarei o valor do resto e imprimir
		int resto;
		resto=5%2;
		
		System.out.println(" O resto entre 5 dividido por 2 é "+ resto +".");
	}
}
